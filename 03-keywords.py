# =====================================================
# Python Keywords
# =====================================================

# Keywords are reserved words in Python.
# They cannot be used as variable names,
# function names, or other identifiers.
# Python keywords are case-sensitive.


# =====================================================
# Logical Operators
# =====================================================

# and        Logical AND
# or         Logical OR
# not        Logical NOT


# =====================================================
# Boolean and Null Values
# =====================================================

# True       Boolean true value
# False      Boolean false value
# None       Represents no value


# =====================================================
# Control Flow
# =====================================================

# if         Conditional statement
# elif       Else-if condition
# else       Alternative condition
# for        For loop
# while      While loop
# break      Exit a loop
# continue   Skip to next loop iteration
# pass       Do nothing


# =====================================================
# Functions and Classes
# =====================================================

# def        Define a function
# return     Return a value from a function
# lambda     Anonymous function
# class      Define a class
# yield      Return values from a generator


# =====================================================
# Exceptions and Error Handling
# =====================================================

# try        Wrap risky code
# except     Handle exceptions
# finally   Always executed block
# raise      Raise an exception
# assert     Debugging check


# =====================================================
# Modules and Imports
# =====================================================

# import     Import a module
# from       Import specific parts
# as         Create an alias


# =====================================================
# Scope and Variables
# =====================================================

# global     Declare global variable
# nonlocal   Declare non-local variable
# del        Delete an object


# =====================================================
# Asynchronous Programming
# =====================================================

# async      Define async function
# await      Wait for async result


# =====================================================
# Pattern Matching and Context
# =====================================================

# match      Start a match statement
# case       Define a match case
# with       Simplify resource handling


# =====================================================
# Membership and Identity
# =====================================================

# in         Check membership
# is         Check object identity
