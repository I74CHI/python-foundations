# =====================================================
# Variables in Python
# =====================================================

# A variable is a name given to a memory location that stores data in a program.


# =====================================================
# Variable Declaration and Assignment
# =====================================================

abhidha = "moti"

name = "Aman Singh"
age = 21
gender = "M"
height = 5.11


# =====================================================
# Printing Variable Values
# =====================================================

print(abhidha)
print(name)
print(age)
print(gender)
print(height)


# =====================================================
# Printing with Labels
# =====================================================

print("My name is:", name)
print("My age is:", age)
print("My gender is:", gender)
print("My height is:", height)


# =====================================================
# Variable Copying
# =====================================================

age2 = age
print(age2)


# =====================================================
# Rules for Identifiers (Variable Names)
# =====================================================

# 1. Can contain letters, digits, and underscore (_)
#    Examples: myVariable, variable_1, variable_for_print
#
# 2. Cannot start with a digit
#    Valid: variable1
#    Invalid: 1variable
#
# 3. Cannot use special symbols
#    !, @, #, $, %, ^, &, etc.
#
# 4. Can be of any length
