# =====================================================
# Data Types in Python
# =====================================================


# =====================================================
# Variable Declaration
# =====================================================

name = "Aman Singh"
age = 22
pi = 3.14


# =====================================================
# Checking Data Types
# =====================================================

print(type(name))   # <class 'str'>
print(type(age))    # <class 'int'>
print(type(pi))     # <class 'float'>


# =====================================================
# Built-in Data Types
# =====================================================

# 1. Integer (int)
#    Whole numbers, positive, negative, or zero
#
# 2. String (str)
#    Characters, letters, symbols, text
#
# 3. Float (float)
#    Decimal numbers
#
# 4. Boolean (bool)
#    True or False
#
# 5. None (NoneType)
#    Represents no valid value
